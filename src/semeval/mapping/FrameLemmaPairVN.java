package semeval.mapping;

public class FrameLemmaPairVN {
	
	String vnclass;
	
	String vnmember;

	public FrameLemmaPairVN(String vnclass, String vnmember) {
		super();
		this.vnclass = vnclass;
		this.vnmember = vnmember;
	}

	public String getVnclass() {
		return vnclass;
	}

	public String getVnmember() {
		return vnmember;
	}
	
	

}
