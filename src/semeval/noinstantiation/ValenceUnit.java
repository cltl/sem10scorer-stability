package semeval.noinstantiation;

public class ValenceUnit {
	
	private String fe;
	
	private String pt;
	
	private String gf;

	public ValenceUnit(String fe, String pt, String gf) {
		super();
		this.fe = fe;
		this.pt = pt;
		this.gf = gf;
	}

	public String getFe() {
		return fe;
	}

	public String getPt() {
		return pt;
	}

	public String getGf() {
		return gf;
	}
	
	

}
